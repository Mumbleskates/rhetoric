package widders.rhetoric;

/**
 * Expresses the level of detail desired in the description of an object.
 * 
 * @author widders
 */
public enum Detail {
  /** A short but complete description of the object */
  BASIC,
  
  /** When the object is specifically examined */
  DETAIL,
  
  /** When the object is seen from the inside */
  INSIDE
}